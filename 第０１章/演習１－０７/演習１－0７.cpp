#include <iostream>				//
using namespace std;				//3つのint型の値の合計と平均
									//
int main (){						//５月１日
	int x;							//成田修之
	int y;
	int z;

	x=56;
	y=34;
	z=12;

	cout <<"xの値は"<<x<<"です。\n";
	cout <<"yの値は"<<y<<"です。\n";
	cout <<"zの値は"<<z<<"です。\n";
	cout <<"合計は"<<x+y+z<<"です。\n";
	cout <<"平均は"<<(x+y+z)/3<<"です。\n";
}
