#include <iostream>
using namespace std;

int main()
{
	//一行ずつ自分の名前を表示（１文字ごとに改行文字を出力）
	cout << "成\n";		//苗字の"成"
	cout << "田\n";		//苗字の"田"
	cout << "修\n";		//名前の"修"
	cout << "之\n";		//名前の"之"

	//返却値0
	return 0;
}
